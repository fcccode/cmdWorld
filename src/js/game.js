  d('<pre>       _\\//\\\\\\________\\/\\\\\\__\\/\\\\\\__\\/\\\\\\_\\/\\\\\\__\\/\\\\\\_____\\//\\\\\\\\\\\\//\\\\\\\\\\____\\//\\\\\\__/\\\\\\__\\/\\\\\\____________\\/\\\\\\____\\/\\\\\\__\\/\\\\\\__</pre>');
