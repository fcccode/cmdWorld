  function d(str=""){
    timeToWait += 100;
    setTimeout(function(){
      if (typeof str === 'function') {
        str();
      } else {
        main.append(str+'<br/>');
      }
    }, timeToWait);
  };

  d(' <span class="green-text">done</span>');
  d('');
